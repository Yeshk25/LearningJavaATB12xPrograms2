package ex_11_While_loop;

public class Lab115_While_loop {
    public static void main(String[] args) {
        //I,C,U

       int i=0; //intialization
        while (i<10){ // before executing verify the condition
            System.out.println(i);
            i++; // increment
        }
        System.out.println("========For Loop =========");
        // same while prog will write in for loop like below above used i below used J
        for(int j=0;j<10;j++){ //j=0 ->intialization ,j<10 -> condition, j++-> increment
            System.out.println(j);
        }




    }
}
