package ex_10_For_Loop;

public class Lab103_For_if_loop {
    // write a program for gift(phone) for dad, above age 18
    public static void main(String[] args) {
        for(int yash=0; yash<18;yash++){
            if(yash>18) {
                System.out.println("gif from dad-phone");
            }else{
                System.out.println("no gift from dad");
            }
        }
    }

}
